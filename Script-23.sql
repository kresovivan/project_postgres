SET part_of_speech = 'Technical Term'
WHERE (part_of_speech IS NULL OR part_of_speech = '') 
    AND term LIKE '%\_%';

-- Для слов с дефисом - обычно составные прилагательные или существительные
UPDATE words 
SET part_of_speech = 'Compound Word'
WHERE (part_of_speech IS NULL OR part_of_speech = '') 
    AND term LIKE '%-%';

-- Обработка окончаний для оставшихся слов
UPDATE words 
SET part_of_speech = CASE
    -- Существительные по суффиксам
    WHEN term LIKE '%ity' THEN 'Noun'
    WHEN term LIKE '%ness' THEN 'Noun'
    WHEN term LIKE '%ment' THEN 'Noun'
    WHEN term LIKE '%tion' THEN 'Noun'
    WHEN term LIKE '%sion' THEN 'Noun'
    WHEN term LIKE '%ance' THEN 'Noun'
    WHEN term LIKE '%ence' THEN 'Noun'
    WHEN term LIKE '%acy' THEN 'Noun'
    WHEN term LIKE '%ship' THEN 'Noun'
    WHEN term LIKE '%hood' THEN 'Noun'
    WHEN term LIKE '%ism' THEN 'Noun'
    WHEN term LIKE '%ist' THEN 'Noun'
    WHEN term LIKE '%er' AND term NOT LIKE '%her' AND term NOT LIKE '%ter' THEN 'Noun'
    WHEN term LIKE '%or' THEN 'Noun'
    WHEN term LIKE '%ian' THEN 'Noun'
    WHEN term LIKE '%ant' AND term NOT LIKE '%cant' THEN 'Noun'
    WHEN term LIKE '%ent' AND term NOT LIKE '%sent' THEN 'Noun'
    
    -- Прилагательные по суффиксам
    WHEN term LIKE '%able' THEN 'Adjective'
    WHEN term LIKE '%ible' THEN 'Adjective'
    WHEN term LIKE '%al' THEN 'Adjective'
    WHEN term LIKE '%ial' THEN 'Adjective'
    WHEN term LIKE '%ical' THEN 'Adjective'
    WHEN term LIKE '%ful' THEN 'Adjective'
    WHEN term LIKE '%less' THEN 'Adjective'
    WHEN term LIKE '%ous' THEN 'Adjective'
    WHEN term LIKE '%ious' THEN 'Adjective'
    WHEN term LIKE '%eous' THEN 'Adjective'
    WHEN term LIKE '%y' AND LENGTH(term) > 2 AND term NOT LIKE '%ly' THEN 'Adjective'
    WHEN term LIKE '%ary' THEN 'Adjective'
    WHEN term LIKE '%ory' THEN 'Adjective'
    WHEN term LIKE '%ish' THEN 'Adjective'
    WHEN term LIKE '%ive' THEN 'Adjective'
    WHEN term LIKE '%ative' THEN 'Adjective'
    WHEN term LIKE '%itive' THEN 'Adjective'
    
    -- Наречия
    WHEN term LIKE '%ly' THEN 'Adverb'
    
    -- Глаголы
    WHEN term LIKE '%ate' AND LENGTH(term) > 4 THEN 'Verb'
    WHEN term LIKE '%en' AND LENGTH(term) > 3 THEN 'Verb'
    WHEN term LIKE '%ify' THEN 'Verb'
    WHEN term LIKE '%ize' THEN 'Verb'
    WHEN term LIKE '%ise' THEN 'Verb'
    
    -- По умолчанию
    ELSE 'Noun'
END
WHERE part_of_speech IS NULL OR part_of_speech = '';

-- Удаляем временную таблицу
DROP TABLE word_pos_dictionary;

-- Проверяем результат
SELECT COUNT(*) as total_words,
       COUNT(CASE WHEN part_of_speech IS NOT NULL THEN 1 END) as words_with_pos,
       COUNT(CASE WHEN part_of_speech IS NULL THEN 1 END) as words_without_pos
FROM words;

