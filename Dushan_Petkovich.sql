============================================
ВАРИАНТЫ ХРАНЕНИЯ
============================================

Начиная с версии SQL Server 2008, существует два разных варианта
хранения, каждый из которых позволяет сохранять объекты LOB и
экономить дисковое пространство. Это следующие варианты:

- хранение данных типа FILESTREAM;
- хранение с использованием разреженных столбцов (sparse columns).

Эти варианты хранения рассматриваются в следующих подразделах.


ХРАНЕНИЕ ДАННЫХ ТИПА FILESTREAM

Как уже упоминалось ранее, SQL Server поддерживает хранение больших
объектов (LOB) посредством типа данных VARBINARY(max). Свойство этого
типа данных таково, что большие двоичные объекты (BLOB) сохраняются
в базе данных. Это обстоятельство может вызвать проблемы с
производительностью в случае хранения очень больших файлов, таких как
аудио- или видеофайлов. В таких случаях эти данные сохраняются вне
базы данных во внешних файлах.

Хранение данных типа FILESTREAM поддерживает управление объектами LOB,
которые сохраняются в файловой системе NTFS. Основным преимуществом
этого типа хранения является то, что хотя данные хранятся вне базы
данных, управляются они базой данных. Таким образом, этот тип хранения
имеет следующие свойства:

- данные типа FILESTREAM можно сохранять с помощью инструкции
  CREATE TABLE, а для работы с этими данными можно использовать
  инструкции для модифицирования данных (SELECT, INSERT, UPDATE
  и DELETE);
- система управления базой данных обеспечивает такой же самый уровень
  безопасности для данных типа FILESTREAM, как и для данных,
  хранящихся внутри базы данных.

Подробно создание данных типа FILESTREAM рассматривается в главе 5.

┌─────────────────────────────────────────────────────────────────────────┐
│ FILESTREAM                                                             │
├─────────────────────────────────────────────────────────────────────────┤
│ • Хранение BLOB (большие двоичные объекты) в файловой системе NTFS    │
│ • Данные хранятся вне базы данных, но управляются базой данных        │
│ • Используется с типом VARBINARY(max)                                 │
│ • Поддерживает SELECT, INSERT, UPDATE, DELETE                         │
│ • Безопасность на уровне базы данных                                  │
│ • Решает проблемы производительности с большими файлами              │
└─────────────────────────────────────────────────────────────────────────┘


РАЗРЕЖЕННЫЕ СТОЛБЦЫ (SPARSE COLUMNS)

Цель варианта хранения, предоставляемого разреженными столбцами,
значительно отличается от цели хранения типа FILESTREAM. Тогда как
целью хранения типа FILESTREAM является хранение объектов LOB вне
базы данных, целью разреженных столбцов является минимизировать
дисковое пространство, занимаемое базой данных. Столбцы этого типа
позволяют оптимизировать хранение столбцов, большинство значений
которых равны NULL. (Значения NULL рассматриваются в конце этой главы.)
При использовании разреженных столбцов для хранения значений NULL
дисковое пространство не требуется, но, с другой стороны, для хранения
значений, отличных от NULL, требуется дополнительно от 2 до 4 байтов,
в зависимости от их типа. По этой причине разработчики Microsoft
рекомендуют использовать разреженные столбцы только в тех случаях,
когда ожидается, по крайней мере, 20% общей экономии дискового
пространства.

Разреженные столбцы определяются таким же образом, как и прочие столбцы
таблицы; аналогично осуществляется и обращение к ним. Это означает,
что для обращения к разреженным столбцам можно использовать инструкции
SELECT, INSERT, UPDATE и DELETE таким же образом, как и при обращении
к обычным столбцам. (Эти инструкции SQL подробно рассматриваются в
главах 6 и 7.) Единственная разница касается создания разреженных
столбцов: для определения конкретного столбца разреженным применяется
аргумент SPARSE после названия столбца, как это показано в данном
примере:

имя_столбца_тип_данных_SPARSE

Несколько разреженных столбцов таблицы можно сгруппировать в набор
столбцов. Такой набор будет альтернативным способом сохранять значения
во всех разреженных столбцах таблицы и обращаться к ним. Дополнительную
информацию о наборах столбцов см. в электронной документации.

┌─────────────────────────────────────────────────────────────────────────┐
│ РАЗРЕЖЕННЫЕ СТОЛБЦЫ (SPARSE)                                          │
├─────────────────────────────────────────────────────────────────────────┤
│ • Оптимизация хранения столбцов с большим количеством NULL            │
│ • NULL не занимают место                                               │
│ • Не-NULL значения требуют дополнительно 2–4 байта                    │
│ • Рекомендуется только при экономии > 20% дискового пространства      │
│ • Синтаксис: column_name data_type SPARSE                             │
│ • Можно группировать в наборы столбцов                                │
└─────────────────────────────────────────────────────────────────────────┘


