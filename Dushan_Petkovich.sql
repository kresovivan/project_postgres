- Если не указано NULL или NOT NULL, а ANSI_NULL_DFLT_ON = ON

NULL НЕ разрешены:
- Если в определении столбца явно указано NOT NULL
- Для столбцов типа TIMESTAMP (всегда NOT NULL)
- Если не указано NULL или NOT NULL, а ANSI_NULL_DFLT_OFF = ON
- Если инструкция SET не активирована → по умолчанию NOT NULL

Параметры SET:
SET ANSI_NULL_DFLT_ON = ON  → NULL по умолчанию
SET ANSI_NULL_DFLT_OFF = ON → NOT NULL по умолчанию


