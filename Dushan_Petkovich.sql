ТИП ДАННЫХ БОЛЬШИХ ОБЪЕКТОВ (LOB)

Тип данных LOB (Large Object — большой объект) используется для хранения
объектов данных размером до 2 Гбайт. Такие объекты обычно применяются
для хранения больших объемов текстовых данных и для загрузки
подключаемых модулей и аудио- и видеофайлов. В языке Transact-SQL
поддерживаются следующие типы данных LOB:

- VARCHAR(max);
- NVARCHAR(max);
- VARBINARY(max).

┌──────────────────┬─────────────────────────────────────────────────────┐
│      Тип         │                    Описание                        │
├──────────────────┼─────────────────────────────────────────────────────┤
│ VARCHAR(max)     │ Большие текстовые данные, до 2 ГБ.                │
├──────────────────┼─────────────────────────────────────────────────────┤
│ NVARCHAR(max)    │ Большие текстовые данные (Unicode), до 2 ГБ.      │
├──────────────────┼─────────────────────────────────────────────────────┤
│ VARBINARY(max)   │ Большие двоичные данные, до 2 ГБ.                 │
└──────────────────┴─────────────────────────────────────────────────────┘

Начиная с версии SQL Server 2005, для обращения к значениям стандартных
типов данных и к значениям типов данных LOB применяется одна и та же
модель программирования. Иными словами, для работы с объектами LOB
можно использовать удобные системные функции и строковые операторы.

В компоненте Database Engine параметр max применяется с типами данных
VARCHAR, NVARCHAR и VARBINARY для определения значений столбцов
переменной длины. Когда вместо явного указания длины значения
используется значение длины по умолчанию max, система анализирует
длину конкретной строки и принимает решение, сохранять ли эту строку
как обычное значение или как значение LOB. Параметр max указывает,
что размер значений столбца может достигать максимального размера LOB
данной системы.

Хотя решение о способе хранения объектов LOB принимается системой,
настройки по умолчанию можно переопределить, используя системную
процедуру sp_tableoption с аргументом LARGE_VALUE_TYPES_OUT_OF_ROW.
Если значение этого аргумента равно 1, то данные в столбцах,
объявленных с использованием параметра max, будут сохраняться отдельно
от остальных данных. Если же значение аргумента равно 0, то компонент
Database Engine сохраняет все значения размером до 8060 байт в строке
таблицы, как обычные данные, а значения большего размера хранятся
вне строки в области хранения объектов LOB.

Начиная с версии SQL Server 2008, для столбцов типа VARBINARY(max)
