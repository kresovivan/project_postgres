├──────────────────┼─────────────────────────────────────────────────────┤
│ BINARY(n)        │ Фиксированная длина, n байт (0 < n < 8000).      │
├──────────────────┼─────────────────────────────────────────────────────┤
│ VARBINARY(n)     │ Переменная длина, до n байт (0 < n < 8000).       │
