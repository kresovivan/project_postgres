ЗНАЧЕНИЕ NULL
============================================

Значение NULL — это специальное значение, которое можно присвоить ячейке
таблицы. Это значение обычно применяется, когда информация в ячейке
неизвестна или неприменима. Например, если неизвестен номер домашнего
телефона служащего компании, рекомендуется присвоить соответствующей
ячейке столбца home_telephone значение NULL.


АРИФМЕТИЧЕСКИЕ ОПЕРАЦИИ С NULL

Если значение любого операнда любого арифметического выражения равно NULL,
значение результата вычисления этого выражения также будет NULL.

Унарные операции:
+ NULL = NULL
- NULL = NULL

Бинарные операции (если A = NULL или B = NULL):
A + B = NULL
A - B = NULL
A * B = NULL
A / B = NULL
A % B = NULL


ОПЕРАЦИИ СРАВНЕНИЯ С NULL

Если значение одного или обоих операндов операции сравнения равно NULL,
результат этой операции также будет NULL.

A = B  → NULL (если A или B = NULL)
A <> B → NULL (если A или B = NULL)
A < B  → NULL (если A или B = NULL)
A > B  → NULL (если A или B = NULL)


