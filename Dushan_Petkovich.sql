остаток строки заполняется пробелами. А количество байтов, занимаемых
строкой типа VARCHAR, всегда равно количеству символов в ней.


ВРЕМЕННЫЕ ТИПЫ ДАННЫХ

В языке Transact-SQL поддерживаются следующие временные типы данных:

- DATETIME;
- SMALLDATETIME;
- DATE;
- TIME;
- DATETIME2;
- DATETIMEOFFSET.

Типы данных DATETIME и SMALLDATETIME применяются для хранения даты
и времени в виде целочисленных значений длиной в 4 и 2 байта
соответственно. Значения типа DATETIME и SMALLDATETIME сохраняются
внутренне как два отдельных числовых значения. Составляющая даты
значений типа DATETIME хранится в диапазоне от 01/01/1753 до
31/12/9999, а соответствующая составляющая значений типа SMALLDATETIME —
в диапазоне от 01/01/1900 до 06/06/2079. Составляющая времени хранится
во втором 4-байтовом (2-байтовом для значений типа SMALLDATETIME) поле
в виде числа трехсотых долей секунды (для DATETIME) или числа минут
(для SMALLDATETIME), истекших после полуночи.

┌──────────────────┬─────────────────────────────────────────────────────┐
│      Тип         │                    Описание                        │
├──────────────────┼─────────────────────────────────────────────────────┤
│ DATETIME         │ 8 байт. Диапазон: 1753-01-01 до 9999-12-31.      │
│                  │ Точность до 1/300 секунды.                         │
├──────────────────┼─────────────────────────────────────────────────────┤
│ SMALLDATETIME    │ 4 байта. Диапазон: 1900-01-01 до 2079-06-06.     │
│                  │ Точность до 1 минуты.                              │
├──────────────────┼─────────────────────────────────────────────────────┤
│ DATE             │ 3 байта. Диапазон: 0001-01-01 до 9999-12-31.     │
│                  │ Только дата, без времени.                          │
├──────────────────┼─────────────────────────────────────────────────────┤
│ TIME             │ 3–5 байт. Точность до 100 наносекунд.             │
│                  │ Только время, без даты.                            │
├──────────────────┼─────────────────────────────────────────────────────┤
│ DATETIME2        │ 6–8 байт. Высокая точность (100 нс).              │
│                  │ Диапазон: 0001-01-01 до 9999-12-31.               │
├──────────────────┼─────────────────────────────────────────────────────┤
│ DATETIMEOFFSET   │ 6–8 байт. То же, что DATETIME2,                   │
│                  │ + поддержка часового пояса.                       │
└──────────────────┴─────────────────────────────────────────────────────┘

Если нужно сохранить только составляющую даты или времени, использование
значений типа DATETIME или SMALLDATETIME несколько неудобно. По этой
причине в SQL Server были введены типы данных DATE и TIME, в которых
хранятся только составляющие даты и времени значений типа DATETIME,
соответственно.

Значения дат в Transact-SQL по умолчанию определены в виде строки
формата 'ммм дд гггг' (например, 'Jan 10 1993'), заключенной в
одинарные или двойные кавычки. (Но относительный порядок составляющих
месяца, дня и года можно изменять с помощью инструкции SET DATEFORMAT.
Кроме этого, система поддерживает числовые значения для составляющей
месяца и разделители / и -.) Подобным образом, значение времени
указывается в 24-часовом формате в виде 'чч:мм' (например, '22:24').

ПРИМЕЧАНИЕ

Язык Transact-SQL поддерживает различные форматы ввода значений типа
DATETIME. Как уже упоминалось, каждая составляющая определяется
отдельно, поэтому значения дат и времени можно указать в любом порядке
или отдельно. Если одна из составляющих не указывается, система
использует для него значение по умолчанию. (Значение по умолчанию
для времени — 12:00 AM (до полудня).)

Пример 4.4. Действительные представления даты

'28/5/1959' (с использованием инструкции SET DATEFORMAT dmy)
'May 28, 1959'
'1959 MAY 28'


ПРОЧИЕ ТИПЫ ДАННЫХ

Язык Transact-SQL поддерживает несколько типов данных, которые не
принадлежат не к одной из вышеперечисленных групп типов данных.
В частности:

- двоичные типы данных;
- битовый тип данных BIT;
- тип данных больших объектов;
- тип данных CURSOR (рассматривается в главе 8);
- тип данных UNIQUEIDENTIFIER;
- тип данных SQL_VARIANT;
- тип данных TABLE (рассматривается в главах 5 и 8);
- тип данных XML (рассматривается в главе 26);
- пространственные типы данных (рассматриваются в главе 27);
- тип данных HIERARCHYID;
- тип данных TIMESTAMP;
- типы данных, определяемые пользователем (рассматриваются в главе 5).

Все эти типы данных рассматриваются в последующих разделах этой главы
(за исключением тех, для которых указано, что они обсуждаются в других
главах).


ДВОИЧНЫЕ И БИТОВЫЕ ТИПЫ ДАННЫХ

К двоичным типам данным принадлежат два типа: BINARY и VARBINARY.
Эти типы данных описывают объекты данных во внутреннем формате системы
и используются для хранения битовых строк. По этой причине значения
этих типов вводятся, используя шестнадцатеричные числа.

Значения битового типа BIT содержат лишь один бит, вследствие чего
в одном байте можно сохранить до восьми значений этого типа. Краткое
описание свойств двоичных и битовых типов данных приводится в табл. 4.3.

