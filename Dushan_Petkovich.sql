/*
Литералы

Литерал — это буквенно-цифровая (строковая), шестнадцатеричная или числовая
константа. Строковая константа содержит один или больше символов
определенного набора символов, заключенных между одинарными (' ')
или двойными (" ") прямыми кавычками. (Константы предпочтительно заключать
в одинарные кавычки, т. к. двойные кавычки используются во многих других
случаях, как мы увидим вскоре.) Чтобы вставить одинарную кавычку в строку,
заключенную в одинарные кавычки, нужно использовать две одинарные кавычки
последовательно. Шестнадцатеричные константы используются для представления
непечатаемых символов и прочих двоичных данных. Они начинаются символами 0x,
за которыми следует четное число буквенных или цифровых символов.

Пример 4.1. Допустимые строковые и шестнадцатеричные константы

'Philadelphia'
"Berkeley, CA 94710"
'9876'
0x53514C0D
'Апостроф представляется таким образом: can''t'

(Обратите внимание в последней строке примера на использование двух
последовательных одинарных кавычек для представления апострофа
в строковых константах.)

Пример 4.2. Недопустимые строковые константы

'AB'C' — нечетное число одинарных кавычек;
'New York' — строка должна быть заключена в одинаковые кавычки,
т. е. одинарные или двойные с каждого конца строки.

К числовым константам относятся все целочисленные значения и значения
с фиксированной и плавающей запятой (точкой).

Пример 4.3. Числовые константы

-130
-130.00
-0.357E5 — экспоненциальное представление, где nEm означает
n умножено на 10 в степени m
*/


/*
============================================
Ограничители, Комментарии, Идентификаторы и
Зарезервированные ключевые слова
============================================

ОГРАНИЧИТЕЛИ

В языке Transact-SQL двойные кавычки имеют два разных применения:
1. Для заключения строк.
2. В качестве ограничителей для идентификаторов с ограничителями
   (delimited identifier).

Идентификатор с ограничителями — это особый вид идентификатора, который
обычно применяется для того, чтобы:
- разрешить использование ключевых слов в качестве идентификаторов;
- разрешить использование пробелов в именах объектов баз данных.

ПРИМЕЧАНИЕ:
Различие между одинарными и двойными кавычками было впервые введено
в стандарте SQL92. Идентификаторы с ограничителями:
- заключаются в двойные кавычки;
- чувствительны к регистру.

В языке Transact-SQL также поддерживается применение квадратных скобок [ ]
вместо двойных кавычек.

Применение двойных кавычек в языке Transact-SQL определяется с помощью
параметра QUOTED_IDENTIFIER инструкции SET.
SET QUOTED_IDENTIFIER ON (значение по умолчанию) — идентификаторы
в двойных кавычках определяются как идентификаторы с ограничителями.

---
КОММЕНТАРИИ

В языке Transact-SQL существует два способа определения комментариев:

1. Многострочный: /* текст комментария */
2. Однострочный: -- текст до конца строки

Способ с двумя дефисами (--) отвечает стандарту ANSI SQL,
а способ /* */ является расширением Transact-SQL.

---
ИДЕНТИФИКАТОРЫ

Идентификаторы применяются для обозначения объектов баз данных
(базы данных, таблицы, индексы и т.д.).

Правила для обычных идентификаторов:
- Длина: до 128 символов.
- Могут содержать: буквы, цифры, символы _, @, #, $.
- Первый символ: буква или _, @, #.

Специальные символы в начале:
- # — временный объект (таблица или процедура)
- @ — переменная

Идентификаторы с ограничителями (в кавычках или скобках):
- могут содержать ЛЮБЫЕ символы;
- могут начинаться с ЛЮБОГО символа (кроме самого ограничителя);
- чувствительны к регистру (в отличие от обычных).

---
ЗАРЕЗЕРВИРОВАННЫЕ КЛЮЧЕВЫЕ СЛОВА

Зарезервированные ключевые слова — это имена, которые требуется писать
и применять в определенном формате. Их нельзя применять в качестве имен
объектов, ЗА ИСКЛЮЧЕНИЕМ случаев, когда они используются как
идентификаторы с ограничителями (в кавычках или скобках).

ВАЖНО: имена типов данных и системных функций (CHARACTER, INTEGER и т.д.)
НЕ являются зарезервированными ключевыми словами. Их можно использовать
для имен объектов, но НАСТОЯТЕЛЬНО НЕ РЕКОМЕНДУЕТСЯ.

---
ТИПЫ ДАННЫХ

Все значения в столбце должны быть одного типа данных.
(Исключение: тип данных SQL_VARIANT.)

Категории типов данных в Transact-SQL:
- Числовые типы
- Символьные типы
- Временные типы (даты и/или времени)
- Прочие типы данных
*/


