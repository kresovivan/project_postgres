ORDER BY
    CASE
        WHEN ServiceCD = 1 THEN PaySum::TEXT
        WHEN ServiceCD = 2 THEN PayDate::TEXT
        WHEN ServiceCD = 3 THEN accountcd::TEXT
        ELSE PaySum::TEXT
        END;